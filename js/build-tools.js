/*
Instalar en pc
Si bien puede instalar Babel CLI globalmente en su máquina, es mucho mejor instalarlo localmente proyecto por proyecto.

Hay dos razones principales para esto.

Diferentes proyectos en la misma máquina pueden depender de diferentes versiones de Babel, lo que le permite actualizarlos individualmente.
No tener una dependencia implícita del entorno en el que está trabajando hace que su proyecto sea mucho más portátil y más fácil de configurar.
Podemos instalar Babel CLI localmente ejecutando:

npm install --save-dev @babel/core @babel/cli
*/